function expandTemplates(seeds, count) {
  const variants = ["simple", "real", "local", "soft", "sharp"];
  const output = [];
  for (let i = 0; output.length < count; i += 1) {
    const seed = seeds[i % seeds.length];
    const variant = variants[Math.floor(i / seeds.length) % variants.length];
    output.push(seed.replace("{style}", variant));
  }
  return output;
}

